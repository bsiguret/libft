#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(dest);
	i = 0;
	while (src[i])
	{
		dest[len] = src[i++];
		++len;
	}
	dest[len] = '\0';
	return (dest);
}
