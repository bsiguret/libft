#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *i;

	i = lst;
	if (lst != NULL || f != NULL)
	{
		while (i)
		{
			f(i);
			i = i->next;
		}
	}
}
